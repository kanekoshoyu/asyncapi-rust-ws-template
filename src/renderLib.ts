import { RenderFile } from './renderFile';
import { AsyncAPIDocumentInterface } from '@asyncapi/parser';

function content_module(nameModule: string, description: string): String {
  return `/// ${description}
mod ${nameModule};
`
}

function content(): string {
  let defineMod: string = "";
  let contents = [content_module("client", "client code"), content_module("model", "model code (generated by modelina)")];

  for (const content of contents) {
    defineMod += content;

  }
  return defineMod;
}

export function renderLibRs(doc: AsyncAPIDocumentInterface): RenderFile {
  return new RenderFile("src/lib.rs", content());

}
